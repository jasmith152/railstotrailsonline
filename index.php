<p><font size="4" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular"><b>Rails to Trails Of The Withlacoochee, Inc<br />
								</b></font><font size="3" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular">is a Citizen's Support Organization and a Friends Group of the DEP Division of Recreation and Parks  for the Withlacoochee, Inc. State Trail. </font></p>
						<p><img src="images/logo-homepage.jpg" alt="Rails to Trails of the Withlacoochee logo" width="90" height="110" align="left" /><font face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular" size="2"><b>Rails to Trails Of The Withlacoochee, Inc</b> is a not-for-profit, charitable organization as qualified under section 501 (c) (3) of the Internal Revenue Code. Our registration with the Department of Agriculture is # SC-00268. <a href="news.html">(See Trail News Story)</a></font></p>
						<p><font face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular" size="2">It is composed of a 46-mile stretch of railroad track that was converted to a paved recreational trail suitable for hiking, biking, and skating, Horseback riding is available on a separate trail along side the paved trail. <a href="images/bikemap.pdf">(See Map)</a> The North end of the trail is located in Citrus Springs south of Dunnellon, Florida and extends 46 miles to the south ending at the trailhead parking at Highway 301 in Dade City, Florida.<br />
							</font></p>
						<p><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular">The Withlacoochee Bicycle Riders are a group of cyclists who ride 3 or 4 times a week and are affiliated with Rails to Trails of the Withlacoochee, Inc.. A ride schedule is available and anyone is welcome to join the group. The group uses the Withlacoochee, Inc. Trail as an access point for these rides. They also ride six other trails within 1 1/2 hr drive of Inverness, Florida.<br />
								  </font></p>
